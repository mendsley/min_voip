#include "tiny/peer/message.h"

using namespace tiny;
using namespace peer;

Message* peer::messageAlloc(uint32_t size)
{
	uint8_t* raw = new uint8_t[sizeof(Message)+size];
	Message* msg = reinterpret_cast<Message*>(raw);
	msg->data = raw + sizeof(Message);
	msg->ndata = size;
	return msg;
}

void peer::messageRelease(Message* msg)
{
	uint8_t* raw = reinterpret_cast<uint8_t*>(msg);
	delete[] raw;
}
